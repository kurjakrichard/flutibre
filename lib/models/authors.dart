class Author {
  int? _id;
  String? _name;
  String? _sort;
  String? _link;
  get id => this._id;

  set id(value) => this._id = value;
  get name => this._name;
  set name(value) => this._name = value;
  get sort => this._sort;
  set sort(value) => this._sort = value;
  get link => this._link;
  set link(value) => this._link = value;
}
